package raj_learning.day1;

public class Example7 {
	public static void main(String []args) {
		
		//Program to print first 25 natural numbers using while loop
		int i = 1;
		while(i<=25) {
			System.out.println(i);
			i++;
		}
		
		System.out.println(" ");
		
		//Program to print first 10 natural numbers using do while loop
	    int j = 1; 
		do {
	    	 
			System.out.println(j);
			j++;
	     }while(j<=10);
	
		//The example below will print the numbers 0 to 4 using for loop
		for(int a=0;a<5;a++) {
			System.out.println(a);
		}
		
	}

}
